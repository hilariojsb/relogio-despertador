/** Artigo genérico para outros presets de minutos (indexação + consistência). */
export function buildMinuteArticle(minutes: number, hasShortcut: boolean): SeoArticle {
  const shortcutHint = hasShortcut
    ? `O atalho +${minutes} min cria rapidamente um alarme relativo ao horário atual, ideal quando você não quer calcular manualmente o relógio.`
    : `Para ${minutes} minutos, combine os atalhos +5, +10 e +30 ou use o campo de horário para definir o momento exato do disparo.`;

  return {
    sections: [
      {
        heading: `Alarme de ${minutes} minutos no navegador`,
        paragraphs: [
          `Programar um despertador de ${minutes} minutos é útil para blocos de foco, cozinha, exercícios ou pequenas pausas na rotina de trabalho. ${shortcutHint} O Relógio Despertador roda no navegador e guarda preferências localmente, sem exigir instalação de aplicativo.`,
          'Mantenha a aba aberta no horário do alerta e ajuste o volume conforme o ambiente. Você pode nomear cada alarme para identificar rapidamente o propósito do lembrete na lista.',
        ],
      },
      {
        heading: 'Boas práticas',
        paragraphs: [
          'Evite criar dezenas de alarmes sobrepostos; prefira poucos horários claros e revise ao fim do dia. Se o som parecer baixo, suba o controle do alarme e confira também o volume do sistema. Para ambientes silenciosos, reduza o nível e faça testes curtos antes de compromissos importantes.',
        ],
      },
    ],
  };
}
